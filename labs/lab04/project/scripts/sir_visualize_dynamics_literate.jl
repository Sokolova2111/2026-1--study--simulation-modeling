# # Модель SIR: Сводная визуализация результатов
# 
# **Цель работы:** Загрузить результаты параметрического сканирования
# (файл `beta_scan_all.csv`, созданный `sir_scan_beta.jl`) и построить
# единый составной график, объединяющий три панели:
# - пик эпидемии и конечная доля инфицированных
# - число умерших
# - доля выздоровевших
# 
# ## 1. Инициализация проекта и загрузка пакетов

using DrWatson
@quickactivate "project"
using Agents, DataFrames, Plots, CSV
include(srcdir("sir_model.jl"))

# ## 2. Загрузка результатов сканирования
# 
# Загружаем CSV-файл, созданный скриптом `sir_scan_beta.jl`.
# Файл содержит результаты всех прогонов для разных значений β.

df = CSV.read(datadir("beta_scan_all.csv"), DataFrame)

# ## 3. Создание составного графика
# 
# Строим три графика вертикально:
# 
# ### Панель 1: Пик и конечная доля инфицированных
# - Синяя кривая — пик эпидемии (максимальная доля инфицированных)
# - Красная кривая — конечная доля инфицированных (к концу эпидемии)

p1 = plot(df.beta, df.peak, 
    label = "Пик", 
    xlabel = "β", 
    ylabel = "Доля инфицированных",
    linewidth = 2,
    color = :blue
)
plot!(p1, df.beta, df.final_inf, 
    label = "Конечная", 
    linewidth = 2,
    color = :red
)

# ### Панель 2: Число умерших
# - Показывает зависимость количества смертей от коэффициента заразности β

p2 = plot(df.beta, df.deaths, 
    xlabel = "β", 
    ylabel = "Число умерших",
    linewidth = 2,
    color = :green,
    label = "Умершие"
)

# ### Панель 3: Доля выздоровевших
# - Показывает, какая часть популяции переболела и приобрела иммунитет

p3 = plot(df.beta, df.final_rec, 
    xlabel = "β", 
    ylabel = "Доля выздоровевших",
    linewidth = 2,
    color = :purple,
    label = "Выздоровевшие"
)

# ## 4. Объединение графиков
# 
# Располагаем три панели вертикально (3 строки, 1 колонка)
# с общим размером 800×900 пикселей.

plot(p1, p2, p3, 
    layout = (3, 1), 
    size = (800, 900)
)

# ## 5. Сохранение графика
# 
# Сохраняем результат в папку `plots/` для последующего включения в отчёт.

savefig(plotsdir("comprehensive_analysis.png"))

# ## 6. Вывод информации о завершении
# 
# Ожидаемый результат: с ростом β пик эпидемии и число умерших увеличиваются,
# а конечная доля инфицированных стремится к порогу коллективного иммунитета.

println("График сохранён в: $(plotsdir("comprehensive_analysis.png"))")